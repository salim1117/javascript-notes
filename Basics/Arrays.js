
// array using []
let stu_info = ["salim" , 17 , "BE" , 2002 ]
console.log(stu_info);

// array using new keyword
let a = new Array("salim", "dady " , 22 , "BE" , 2004 )
console.log(a);

// this will push to last 
a.push("Hello");
console.log(a);

// pop remove the last element
a.pop();
console.log(a);

// this will push to first
a.unshift("dj");
console.log(a);

// shift remove the first element
a.shift();
console.log(a);

// accessing the elements by index
console.log(a[0]);






